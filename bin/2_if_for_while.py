# while
# ----------------
# Based on condition if we want to execute some loop
i = 0
while i < 10:
    print("i : ", i)
    i = i + 1

s = "Python"
j = 0
while j < len(s):
    print("j : ", s[j])
    j = j + 1

# We can use
# break to stop while loop in between
# continue to skip current iteration and goto next iteration
